package com.example.libraryapp;

public class Book {
    String Name;
    String Price;
    String Synopsis;
    String Author;
    String Type;

    public String getType() {
        return Type;
    }

    public String getName() {
        return Name;
    }

    public String getPrice() {
        return Price;
    }

    public String getSynopsis() {
        return Synopsis;
    }

    public String getAuthor() {
        return Author;
    }
}
